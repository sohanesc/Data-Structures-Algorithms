# Escape Sequence
# \n : new line
# \t : tab
# \\ : backslash
# \' : single quote
# \" : double quote
# \b : backspace
# \r : carriage return

print("Hello\nWorld")
print("Hello\tWorld")
print("Hello\\World")
print("Hello'World")
print('Hello"World')
print("Hello\bWorld")
print("Hello\rWorld")

# Py/\th\\//on

print("Py/\\th\\\\//on")
